/**
 * 根据单词和武器类型获取 Jarvis 教学提示
 * @param {string} wordKey - 单词（小写）
 * @param {string} weaponId - 武器类型: syllables | mnemonic | image | etymology
 * @returns {object} Jarvis 教学提示对象 { title, content, action }
 */
export const getWeaponJarvisScript = (wordKey, weaponId) => {
  if (!wordKey || !weaponId) {
    return {
      title: '武器库辅助',
      content: '当前武器库已打开，观察学生学习情况。',
      action: '根据学生反应决定是否需要进一步讲解。',
    };
  }

  const wordScripts = weaponJarvisScripts[wordKey];
  if (!wordScripts || !wordScripts[weaponId]) {
    return {
      title: '武器库辅助',
      content: `正在使用${weaponId}武器帮助学生记忆单词 ${wordKey}。`,
      action: '观察学生的理解程度，适时给予鼓励。',
    };
  }

  return wordScripts[weaponId];
};

export default weaponJarvisScripts;
